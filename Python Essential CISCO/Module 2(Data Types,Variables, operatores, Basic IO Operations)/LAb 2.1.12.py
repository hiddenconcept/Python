# Expected outcome:
# Programming***Essentials***in...Python


print("Programming","Essentials","in", sep="***", end="...")
print("Python")

# Explanation
# sep="***" changes the separator between the printed values from the default space (" ") to ***.
# end="..." changes what print() outputs at the end. Instead of a newline (\n), it prints ....
# The second print("Python") continues immediately after the ... and then ends with a newline.